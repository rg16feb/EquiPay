package com.equipay.equipay;

public class OccasionTable {
    final String TableName = "Occasions";
    final String occasionID = "OccasionID";
    final String occasionName = "OccasionName";
    final String dateOfOccasion = "DateOfOccasion";
    final String members = "Members";

    public String getMembers() {
        return members;
    }

    public String getTableName() {
        return TableName;
    }

    public String getOccasionID() {
        return occasionID;
    }

    public String getOccasionName() {
        return occasionName;
    }

    public String getDateOfOccasion() {
        return dateOfOccasion;
    }
}
